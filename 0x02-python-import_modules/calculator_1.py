#!/usr/bin/python3

def add(a, b):
    """function: addition

    Args:
        a: integer one
        b: integer two

    Returns:
        a + b return value.
    """
    return (a + b)


def sub(a, b):
    """function: subtraction

    Args:
        a: integer one
        b: integer two

    Returns:
        a - b return value
    """
    return (a - b)


def mul(a, b):
    """function: Multiplication

    Args:
        a: integer one
        b: integer two

    Returns:
        a * b return value
    """
    return (a * b)


def div(a, b):
    """function: Division

    Args:
        a: integer one
        b: integer two

    Returns:
        a / b return value
    """
    return int(a / b)
